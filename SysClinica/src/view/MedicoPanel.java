    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Medico m =
        new Medico();

        m.setNome(
            txtNome.getText());

        m.setCrm(
            txtCrm.getText());

        m.setTelefone(
            txtTelefone.getText());

        m.setEmail(
            txtEmail.getText());

        m.setEspecialidade(
            txtEspecialidade.getText());

        MedicoDAO dao =
        new MedicoDAO();

        if(dao.salvar(m)){

            JOptionPane.showMessageDialog(
                this,
                "Médico cadastrado!"
            );

            carregarTabela();

        }else{

            JOptionPane.showMessageDialog(
                this,
                "Erro ao cadastrar!"
            );
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        Medico m =
        new Medico();

        m.setId(
            Integer.parseInt(
                txtId.getText()
            )
        );

        m.setNome(
            txtNome.getText());

        m.setCrm(
            txtCrm.getText());

        m.setTelefone(
            txtTelefone.getText());

        m.setEmail(
            txtEmail.getText());

        m.setEspecialidade(
            txtEspecialidade.getText());

        MedicoDAO dao =
        new MedicoDAO();

        if(dao.atualizar(m)){

            JOptionPane.showMessageDialog(
                this,
                "Atualizado com Sucesso!"
            );

            carregarTabela();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int id =
            Integer.parseInt(
                txtId.getText()
            );

        MedicoDAO dao =
                new MedicoDAO();

        if(dao.eliminar(id)){

            JOptionPane.showMessageDialog(
                    this,
                    "Paciente eliminado!"
            );

            carregarTabela();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

