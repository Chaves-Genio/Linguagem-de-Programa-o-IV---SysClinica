    private void carregarTabela(){

        DefaultTableModel modelo;
            modelo = (DefaultTableModel)
                    tblMedicos.getModel();

        modelo.setRowCount(0);

        MedicoDAO dao =
                new MedicoDAO();

        for(Medico m : dao.listar()){

            modelo.addRow(new Object[]{
                m.getId(),
                m.getNome(),
                m.getCrm(),
                m.getEspecialidade(),
                m.getTelefone(),
                m.getEmail(),
            });
        }
    }
    
