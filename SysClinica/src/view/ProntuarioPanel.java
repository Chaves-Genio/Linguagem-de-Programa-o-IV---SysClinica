    private void tblProntuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProntuarioMouseClicked
        // TODO add your handling code here:
        int linha = tblProntuario.getSelectedRow();

        if (linha != -1) {

            idProntuarioSelecionado =
                    Integer.parseInt(tblProntuario.getValueAt(linha, 0).toString());

            cbConsulta.setSelectedItem(tblProntuario.getValueAt(linha, 1).toString());
            txtDiagnostico.setText(tblProntuario.getValueAt(linha, 2).toString());
            txtMedicamentos.setText(tblProntuario.getValueAt(linha, 3).toString());
            txtObservacoes.setText(tblProntuario.getValueAt(linha, 4).toString());
        }

    }//GEN-LAST:event_tblProntuarioMouseClicked


