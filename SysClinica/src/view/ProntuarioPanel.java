    private void listarProntuarios() {

        DefaultTableModel modelo =
                (DefaultTableModel) tblProntuario.getModel();

        modelo.setRowCount(0);

        ProntuarioDAO dao = new ProntuarioDAO();

        for (Prontuario p : dao.listar()) {

            modelo.addRow(new Object[]{

                p.getId(),
                p.getConsultaId(),
                p.getDiagnostico(),
                p.getMedicamentos(),
                p.getObservacoes()
            });
        }
    }
