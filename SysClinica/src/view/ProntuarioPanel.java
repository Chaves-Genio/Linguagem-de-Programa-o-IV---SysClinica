    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
         try {

            Prontuario p = new Prontuario();

            p.setConsultaId(Integer.parseInt(cbConsulta.getSelectedItem().toString()));
            p.setDiagnostico(txtDiagnostico.getText());
            p.setMedicamentos(txtMedicamentos.getText());
            p.setObservacoes(txtObservacoes.getText());

            ProntuarioDAO dao = new ProntuarioDAO();

            if (dao.salvar(p)) {

                JOptionPane.showMessageDialog(this,
                        "Prontuário salvo com sucesso!");

                listarProntuarios();
                limparCampos();

            } else {

                JOptionPane.showMessageDialog(this,
                        "Erro ao salvar prontuário!");
            }

        } catch (HeadlessException | NumberFormatException e) {

            JOptionPane.showMessageDialog(this,
                    "Erro: " + e.getMessage());
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if (idProntuarioSelecionado == 0) {

            JOptionPane.showMessageDialog(this,
                    "Selecione um prontuário!");
            return;
        }

        try {

            Prontuario p = new Prontuario();

            p.setId(idProntuarioSelecionado);
            p.setConsultaId(Integer.parseInt(cbConsulta.getSelectedItem().toString()));
            p.setDiagnostico(txtDiagnostico.getText());
            p.setMedicamentos(txtMedicamentos.getText());
            p.setObservacoes(txtObservacoes.getText());

            ProntuarioDAO dao = new ProntuarioDAO();

            if (dao.atualizar(p)) {

                JOptionPane.showMessageDialog(this,
                        "Prontuário atualizado com sucesso!");

                listarProntuarios();
                limparCampos();

            } else {

                JOptionPane.showMessageDialog(this,
                        "Erro ao atualizar prontuário!");
            }

        } catch (HeadlessException | NumberFormatException e) {

            JOptionPane.showMessageDialog(this,
                    "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

         if (idProntuarioSelecionado == 0) {

            JOptionPane.showMessageDialog(this,
                    "Selecione um prontuário!");
            return;
        }

        int op = JOptionPane.showConfirmDialog(
                this,
                "Deseja eliminar este prontuário?",
                "Confirmação",
                JOptionPane.YES_NO_OPTION
        );

        if (op == JOptionPane.YES_OPTION) {

            ProntuarioDAO dao = new ProntuarioDAO();

            if (dao.eliminar(idProntuarioSelecionado)) {

                JOptionPane.showMessageDialog(this,
                        "Prontuário eliminado!");

                listarProntuarios();
                limparCampos();
                idProntuarioSelecionado = 0;

            } else {

                JOptionPane.showMessageDialog(this,
                        "Erro ao eliminar!");
            }
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

