    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {

            PacienteDAO pacienteDAO = new PacienteDAO();
            MedicoDAO medicoDAO = new MedicoDAO();

            String nomePaciente = cbPaciente.getSelectedItem().toString();
            String nomeMedico = cbMedico.getSelectedItem().toString();

            Paciente paciente = pacienteDAO.buscarPorNome(nomePaciente);
            Medico medico = medicoDAO.buscarPorNome(nomeMedico);

            Consulta c = new Consulta();

            c.setPacienteId(paciente.getId());
            c.setMedicoId(medico.getId());

            c.setDataConsulta(
                    java.sql.Date.valueOf(txtData.getText()));

            c.setHoraConsulta(
                    java.sql.Time.valueOf(txtHora.getText()));

            c.setStatus(cbStatus.getSelectedItem().toString());

            ConsultaDAO dao = new ConsultaDAO();

            if (dao.salvar(c)) {

                JOptionPane.showMessageDialog(
                        this,
                        "Consulta registada com sucesso!");

                listarConsultas();

            } else {

                JOptionPane.showMessageDialog(
                        this,
                        "Erro ao guardar!");
            }

        } catch (HeadlessException e) {

            JOptionPane.showMessageDialog(
                    this,
                    "Erro: " + e.getMessage());
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if (idConsultaSelecionada == 0) {

            JOptionPane.showMessageDialog(this,
                    "Selecione uma consulta!");

            return;
        }

        try {

            PacienteDAO pacienteDAO = new PacienteDAO();
            MedicoDAO medicoDAO = new MedicoDAO();

            String nomePaciente = cbPaciente.getSelectedItem().toString();
            String nomeMedico = cbMedico.getSelectedItem().toString();

            Paciente paciente = pacienteDAO.buscarPorNome(nomePaciente);
            Medico medico = medicoDAO.buscarPorNome(nomeMedico);

            if (paciente == null || medico == null) {
                JOptionPane.showMessageDialog(this,
                        "Paciente ou Médico inválido!");
                return;
            }

            java.sql.Date data;
            java.sql.Time hora;

            try {
                data = java.sql.Date.valueOf(txtData.getText().trim());
                hora = java.sql.Time.valueOf(
                        txtHora.getText().trim().length() == 5
                                ? txtHora.getText().trim() + ":00"
                                : txtHora.getText().trim()
                );
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Data ou hora inválida!");
                return;
            }

            Consulta c = new Consulta();

            c.setId(idConsultaSelecionada);
            c.setPacienteId(paciente.getId());
            c.setMedicoId(medico.getId());
            c.setDataConsulta(data);
            c.setHoraConsulta(hora);
            c.setStatus(cbStatus.getSelectedItem().toString());

            ConsultaDAO dao = new ConsultaDAO();

            if (dao.atualizar(c)) {

                JOptionPane.showMessageDialog(this,
                        "Consulta atualizada com sucesso!");

                listarConsultas();
                limparCampos();

            } else {

                JOptionPane.showMessageDialog(this,
                        "Erro ao atualizar consulta!");
            }

        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this,
                    "Erro: " + e.getMessage());
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        if (idConsultaSelecionada == 0) {

            JOptionPane.showMessageDialog(this,
                    "Selecione uma consulta!");

            return;
        }

        int op = JOptionPane.showConfirmDialog(
                this,
                "Deseja realmente eliminar esta consulta?",
                "Confirmação",
                JOptionPane.YES_NO_OPTION
        );

        if (op == JOptionPane.YES_OPTION) {

            ConsultaDAO dao = new ConsultaDAO();

            if (dao.eliminar(idConsultaSelecionada)) {

                JOptionPane.showMessageDialog(this,
                        "Consulta eliminada com sucesso!");

                listarConsultas();
                limparCampos();
                idConsultaSelecionada = 0;

            } else {

                JOptionPane.showMessageDialog(this,
                        "Erro ao eliminar consulta!");
            }
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

