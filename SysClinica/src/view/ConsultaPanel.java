    private void pesquisarConsultas() {

        DefaultTableModel modelo =
                (DefaultTableModel) tblConsultas.getModel();

        modelo.setRowCount(0);

        ConsultaDAO dao = new ConsultaDAO();

        PacienteDAO pacienteDAO = new PacienteDAO();
        MedicoDAO medicoDAO = new MedicoDAO();

        String texto = txtPesquisar.getText().trim();

        for (Consulta c : dao.pesquisar(texto)) {

            Paciente p = pacienteDAO.buscarPorId(c.getPacienteId());
            Medico m = medicoDAO.buscarPorId(c.getMedicoId());

            modelo.addRow(new Object[]{

                c.getId(),
                (p != null ? p.getNome() : ""),
                (m != null ? m.getNome() : ""),
                c.getDataConsulta(),
                c.getHoraConsulta(),
                c.getStatus()
            });
        }
    }
    
    
    private void limparCampos() {

        cbPaciente.setSelectedIndex(0);
        cbMedico.setSelectedIndex(0);
        txtData.setText("");
        txtHora.setText("");
        cbStatus.setSelectedIndex(0);

        idConsultaSelecionada = 0;
    }


    private void carregarPacientes() {

        PacienteDAO dao = new PacienteDAO();

        cbPaciente.removeAllItems();

        for(Paciente p : dao.listar()) {
            cbPaciente.addItem(p.getNome());
        }
    }
        
    private void carregarMedicos() {

        MedicoDAO dao = new MedicoDAO();

        cbMedico.removeAllItems();

        for(Medico m : dao.listar()) {
            cbMedico.addItem(m.getNome());
        }
    }
    
    
    private void listarConsultas() {

        DefaultTableModel modelo =
                (DefaultTableModel) tblConsultas.getModel();

        modelo.setRowCount(0);

        ConsultaDAO dao = new ConsultaDAO();
        PacienteDAO pacienteDAO = new PacienteDAO();
        MedicoDAO medicoDAO = new MedicoDAO();

        for (Consulta c : dao.listar()) {

            Paciente p = pacienteDAO.buscarPorId(c.getPacienteId());
            Medico m = medicoDAO.buscarPorId(c.getMedicoId());

            modelo.addRow(new Object[]{

                c.getId(),
                (p != null ? p.getNome() : "N/D"),
                (m != null ? m.getNome() : "N/D"),
                c.getDataConsulta(),
                c.getHoraConsulta(),
                c.getStatus()
            });
        }
    }

